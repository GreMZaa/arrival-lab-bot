def _format_program(key: str) -> str:
    """Форматирует описание программы для отправки."""
    p = PROGRAMS[key]
    lines = [f"<b>{p['title']}</b>", f"\n💰 Стоимость: {p['price']}\n"]

    if "extra" in p:
        lines.append(f"ℹ️ {p['extra']}\n")

    lines.append("Что входит:")
    for item in p["items"]:
        if item == "Всё из START" or item == "Всё из LAUNCH":
            lines.append(f"  ➕ {item}")
        else:
            lines.append(f"  ✅ {item}")

    lines.append(f"\n🎯 <b>Результат:</b>\n{p['result']}")
    return "\n".join(lines)


# ── Хэндлеры ──────────────────────────────────────────────

@router.message(lambda m: m.text in BUTTON_TO_KEY)
async def show_program(message: types.Message):
    key = BUTTON_TO_KEY[message.text]
    text = _format_program(key)
    await message.answer(
        text,
        parse_mode="HTML",
        reply_markup=program_actions(key),
    )


@router.message(Command("menu"))
async def cmd_menu(message: types.Message):
    await message.answer(
        "🏠 <b>Главное меню</b>\n\nВыберите вариант, который лучше всего подходит именно вам.",
        parse_mode="HTML",
        reply_markup=main_menu,
    )


@router.message(lambda m: m.text == "🏠 Главное меню")
async def back_to_main_menu(message: types.Message):
    await cmd_menu(message)
